class Employee :
    language ="Py" # This is a class attribute
    salary=120000
ilma=Employee()
ilma.name="Ilma" # this is an instance attribute
print(ilma.name,ilma.language,ilma.salary)
rohan=Employee()  
rohan.name="Rohan Roro robinson"
print(rohan.name ,rohan.salary,rohan.language)

#Here name is instance attribute and salary and language is class 
# attributes as they directly belongs to the class 